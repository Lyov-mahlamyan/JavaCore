package chapters;

public class IfSample {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        if (x < y) System.out.println(40 + 50);


        x = x * 2;
        if (x == y)
            System.out.println(100);

        x = x * 2;
        if (x > y)
            System.out.println(200);

        if (x == y)

            System.out.println(300);
    }
}
